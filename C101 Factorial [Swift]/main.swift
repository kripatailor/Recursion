func factorial(n: Int)-> Int {   /// making a function called factorial  
    print("Calculating factorial(\(n))...") /// printing the calculation factorai l
    if n <= 1{
        print("Terminal case, returning 1.") /// returing terminal case 1
        return 1             /// return 1
    }else{
        let result = n * factorial(n:n - 1)  /// n times the factorial 
        print("Non-terminal case, returning \(result).")  /// returning the non-terminal result
        return result   /// return result
    }

}
print("The final result is: \(factorial(n:n)).") //// printing the final result  
