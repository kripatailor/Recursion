func factorial(n: Int) -> Int {    //// making a function called factorial   
    print("Calculating fibonacci(\(n))...")   ////// printing the calculating fibonacci     
    
    if(n <= 2) {       //// if n is less that or equal to  
        print("Terminal case, returning 1.")   //// returning terminal case 1   
        return 1 /// returning 1   
    }
    else{
        let result = factorial(n:n-1) + factorial(n:n - 2)  //// letting result equal factorial   
        print("Non-terminal case, returning \(result).")   ///// returning the result for the non terminal case 
        return result ///// return result      
    }
}
print("The final result is: \(factorial(n:n)).")    //// printing the final result

